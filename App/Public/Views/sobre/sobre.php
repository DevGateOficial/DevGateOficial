  <div class="introducao">
    <h2 class="titulo-intro">DevGate - Plataforma de ensino</h2>
    <p class="text">
      “Todos neste país deveriam aprender a programar um computador, pois
      isto ensina a pensar.” Com essa frase de Steve Jobs, conseguimos ter
      uma ideia da importância da programação na sociedade moderna.
    </p>
    <p class="text">
      A plataforma de ensino desenvolvida tem como objetivo principal
      facilitar o processo de aprendizagem para estudantes iniciantes na
      área de programação. Para atingir esse objetivo, ela reúne e organiza
      cursos focados na área de informática, permitindo que os estudantes
      tenham acesso a uma ampla variedade de recursos de aprendizagem.
    </p>
    <p class="text">
      Como forma de atingir este objetivo, a plataforma permite a criação de
      cursos personalizados dentro dela, o que dá aos professores e tutores
      a flexibilidade de criar programas de ensino que atendam às
      necessidades específicas de seus alunos. Isso também ajuda a garantir
      que os estudantes estejam sempre aprendendo conteúdo relevante e
      atualizado.
    </p>
  </div>
  <div class="objetivos">
    <h2 class="obj-titulo">Objetivo geral</h2>
    <p>
      Desenvolver uma plataforma de ensino de informática, utilizando de
      cursos como ferramentas de ensino.
    </p>
    <div class="objs-especificos">
      <h2 class="obj-titulo">Objetivos específicos</h2>
      <ul>
        <li>Criação de uma plataforma de ensino com cursos;</li>
        <li>
          Organizar e guiar os estudantes, principalmente iniciantes, na
          área da programação;
        </li>
        <li>
          Permitir a criação de cursos, aulas e atividades por parte dos
          professores e administradores da plataforma;
        </li>
        <li>
          Moderar, com objetivo de criação ou alteração dos cursos
          propostos, assim como moderação dos usuários.
        </li>
      </ul>
    </div>
  </div>
